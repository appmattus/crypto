import kotlin.test.Test

class XXHash64Test {

    @Test
    fun xxh64a() {
        // From https://github.com/ekpyron/xxhashct/blob/master/test.cpp
        testKat(
            XXHash64(),
            "",
            "EF46DB3751D8E999"
        )
        testKat(
            XXHash64(2654435761),
            "",
            "AC75FDA2929B17EF"
        )
        testKat(
            XXHash64(),
            "test",
            "4FDCCA5DDB678139"
        )
        testKat(
            XXHash64(2654435761),
            "test",
            "5A183B8150E2F651"
        )
    }

    @Test
    fun xxh64() {
        // From https://pypi.org/project/xxhash/
        testKat(
            XXHash64(),
            "xxhash",
            "32dd38952c4bc720"
        )
        testKat(
            XXHash64(20141025),
            "xxhash",
            "b559b98d844e0635"
        )

        testKat(
            XXHash64(0),
            "I want an unsigned 64-bit seed!",
            "d4cb0a70a2b8c7c1"
        )
        testKat(
            XXHash64(1),
            "I want an unsigned 64-bit seed!",
            "ce5087f12470d961"
        )
    }

    @Test
    fun xxh64looping() {
        // From https://github.com/OpenHFT/Zero-Allocation-Hashing/blob/master/src/test/java/net/openhft/hashing/XxHashTest.java
        HASHES_OF_LOOPING_BYTES_WITHOUT_SEED.forEachIndexed { index, expected ->
            try {
                testKat(
                    XXHash64(0),
                    ByteArray(index) { it.toByte() },
                    expected
                )
            } catch (expected: Error) {
                println("Failed on $index")
                throw expected
            }
        }
    }

    @Test
    fun xxh64loopingSeeded() {
        // From https://github.com/OpenHFT/Zero-Allocation-Hashing/blob/master/src/test/java/net/openhft/hashing/XxHashTest.java
        HASHES_OF_LOOPING_BYTES_WITH_SEED_42.forEachIndexed { index, expected ->
            try {
                testKat(
                    XXHash64(42),
                    ByteArray(index) { it.toByte() },
                    expected
                )
            } catch (expected: Error) {
                println("Failed on $index")
                throw expected
            }
        }
    }

