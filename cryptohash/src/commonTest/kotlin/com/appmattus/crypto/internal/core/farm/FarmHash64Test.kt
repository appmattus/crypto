    @Test
    fun baseTest() {
        for (i in 0 until kTestSize - 1) {
            testKat(data.copyOfRange(i * i, i * i + i), farmHash64Expected[i])
            testKat(data.copyOfRange(i * i, i * i + i), farmHash64WithSeedExpected[i], Algorithm.FarmHash64.Seed(1234567u))
            testKat(data.copyOfRange(i * i, i * i + i), farmHash64WithSeedsExpected[i], Algorithm.FarmHash64.Seeds(1234567u, 0u))
        }

        testKat(data, farmHash64Expected[kTestSize - 1])
        testKat(data, farmHash64WithSeedExpected[kTestSize - 1], Algorithm.FarmHash64.Seed(1234567u))
        testKat(data, farmHash64WithSeedsExpected[kTestSize - 1], Algorithm.FarmHash64.Seeds(1234567u, 0u))
    }

    private fun testKat(data: String, expected: String, algorithm: Algorithm.FarmHash64 = Algorithm.FarmHash64()) {
        testKat({ algorithm.createDigest() }, data, expected)
    }

    private fun testKat(data: ByteArray, expected: String, algorithm: Algorithm.FarmHash64 = Algorithm.FarmHash64()) {
        testKat({ algorithm.createDigest() }, data, expected)
    }

